CREATE TABLE IF NOT EXISTS bsc_watch_addresses (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  address VARCHAR(128) NOT NULL UNIQUE,
  status TINYINT NOT NULL DEFAULT 1,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS bsc_asset_balances (
  address VARCHAR(128) NOT NULL,
  asset_code VARCHAR(16) NOT NULL,
  balance DECIMAL(36,18) NOT NULL DEFAULT 0,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (address, asset_code),
  KEY idx_bsc_asset_code_updated_at (asset_code, updated_at)
);

